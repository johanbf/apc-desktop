    private void btnGuardarClienteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGuardarClienteActionPerformed
        // TODO add your handling code here:
        ClienteController clienteController = new ClienteController();
        
        String nombre = txtNombreCliente.getText();
        String direccion = txtDireccionCliente.getText();
        String telefono = txtTelefonoCliente.getText();
        String tipoCliente = selectTipoCliente.getSelectedItem().toString();
        //System.out.println("tipo cliente "+tipoCliente);
        boolean registro = clienteController.guardarCliente(nombre, direccion, telefono, tipoCliente);
        if(registro){
            CotizacionView cotizacionView = new CotizacionView();
            cotizacionView.setVisible(true);
            this.dispose();
        }
    }//GEN-LAST:event_btnGuardarClienteActionPerformed

